def search_fields(request, list):
    search_result = [i for i in list if request.POST['title']  in i.title and 
                    request.POST['first_name']  in i.owner.first_name and 
                    request.POST['last_name']  in i.owner.last_name and 
                    request.POST['level']  in i.level and request.POST['year'] in str(i.year)
                ]
    return search_result


